/*
Here we will find the area of a rectangle

example:

length of rectangle = 20cm
breadth of the rectangle = 10cm

area of rectangle formula = l*b 

Ans : 20*10 = 200cm

*/
#include<iostream>

using namespace std;

int main(){

    int length = 20;
    int breadth = 10;
    int area;
    area = length*breadth;
    
    cout<<  area << endl;
    return 0;

}