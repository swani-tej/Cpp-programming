#include<iostream>

using namespace std;

int main(){
    cout<<"Print A five times"<<endl;
    for (int i = 0; i<5; i++) {

        cout<<"A"<<endl;
    }
    cout<<"Print numbers 1 to 5"<<endl;
    for (int i = 1; i<=5; i++) {

        cout<<i<<endl;
    }
    cout<<"Print 5 to 1"<<endl;
    for (int i = 5; i>=1; i--) {

        cout<<i<<endl;
    }
    return 0;

}